void solve() {
    string b;
    cin >> b;
    string res = b.substr(0, 2); 
    for (int i = 3; i < b.size(); i += 2) {
        res += b[i];
    }    
    cout << res << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int t = 1;
    cin >> t;
    
    while (t--) {
        solve();
    }
    
    return 0;
}

